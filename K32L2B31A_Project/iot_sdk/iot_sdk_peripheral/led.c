 void encender_led_verde(){
	 //encender leds verde
	 GPIO_PinWrite(GPIOD,5,0);
 }
 
void apagar_led_verde(){
	//apagar leds verde
	 GPIO_PinWrite(GPIOD,5,1);
}
void encender_led_rojo(){
	 //encender led rojo
	 GPIO_PinWrite(GPIOE,31,0);
 }
void apagar_led_rojo(){
	 //apagar led rojo
	 GPIO_PinWrite(GPIOE,31,1);
 }

